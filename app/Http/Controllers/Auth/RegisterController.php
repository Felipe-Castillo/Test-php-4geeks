    protected function validator(array $data)
    {
        $messages = [
        'first_name.required'=>'Nombre necesario' ,
        'email.required'=>'Email necesario',
        'email.unique'=>'Enviar mensaje por correo electr칩nico',
        'password.required'=>'Contrase침a necesario',
        'password.min'=>'La contrase침a debe tener al menos 6 caracteres.',
        'confirm_password.required'=>'Vuelve a escribir contrase침a necesario',
        ];
        return Validator::make($data, [
            'first_name' => 'required|max:255',
            'email' => 'required|email|max:255|unique:users',
            'password' => 'required|min:6',
            'confirm_password' => 'required|same:password',
        ],$messages);
    }

    /**
     * Create a new user instance after a valid registration.
     *
     * @param  array  $data
     * @return User
     */
    protected function create(array $data)
    {
         // Default User Type "User"
        $role = Role::where('name','=','User')->first();//Obtengo un rol
        //Creamos el usuario
        $user = User::create([
            'first_name' => $data['first_name'],
            'last_name' => $data['last_name'],
            'email' => $data['email'],
            'password' => bcrypt($data['password']),
        ]);

        $user->roles()->attach($role);

        
        return $user;
    }
